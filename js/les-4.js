//shorthands (updating a variable with its current value);

let number = 10;

//number = number + 5; // 15 ->15
number +=5; //number = number + 5 -> 10
number -=5; //number = number - 5 -> 5
number *=5; //number = number * 5 -> 50
number /=5; //number = number / 5 10
number %=5; //number = number % 5 ->0
number **=5; // number = number ** 5 ->0
console.log(number);

